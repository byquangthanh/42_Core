#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;

	ptr = malloc(count * size);
	if (!ptr)
		return (NULL);
	if (ptr != NULL)
	{
		ft_bzero(ptr, count);
	}
	return (ptr);
}
